// Format number with space separators (e.g. 1 000 000)
function F(n: number): string {
  return n.toLocaleString('ru-RU')
}

// Format date string to human-readable
function fmtDate(d: string, lang: 'kk' | 'ru'): string {
  if (!d) return '___'
  const p = d.split('-')
  if (p.length === 3) {
    const months_kk = ['қаңтар', 'ақпан', 'наурыз', 'сәуір', 'мамыр', 'маусым', 'шілде', 'тамыз', 'қыркүйек', 'қазан', 'қараша', 'желтоқсан']
    const months_ru = ['января', 'февраля', 'марта', 'апреля', 'мая', 'июня', 'июля', 'августа', 'сентября', 'октября', 'ноября', 'декабря']
    const mi = parseInt(p[1]) - 1
    return `${parseInt(p[2])} ${lang === 'ru' ? months_ru[mi] : months_kk[mi]} ${p[0]} ж.`
  }
  return d
}

// Get field value with fallback
function gv(values: Record<string, string>, id: string): string {
  return (values[id] || '').trim()
}

